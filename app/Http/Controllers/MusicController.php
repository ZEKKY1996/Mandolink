    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $music = new Music();
        $music->YouTube_link = $request->input('YouTube_link');
        $music->music_name = $request->input('music_name');
        $music->composer_name = $request->input('composer_name');
        $music->arranger_name = $request->input('arranger_name');
        $music->performer_name = $request->input('performer_name');
        $music->channel_name = $request->input('channel_name');
        $music->save();
        
        return redirect()->route('musics.show', ['id' => $music->id]);
    }

